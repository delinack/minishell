#include "minishell.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s2;

	i = -1;
	s2 = malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (s2 == NULL)
		return (0);
	while (s1[++i])
		s2[i] = s1[i];
	s2[i] = '\0';
	return (s2);
}
